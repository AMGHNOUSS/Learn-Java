import java.util.ArrayList;

public class Colors {
    public static void main(String[] args) {
        ArrayList<String> colors = new ArrayList<String>();
        colors.add("Red");
        colors.add("Yellow");
        colors.add("Green");
        colors.add("Gray");
        for (String string : colors) {
            System.out.println(string);
        }
    }
}
